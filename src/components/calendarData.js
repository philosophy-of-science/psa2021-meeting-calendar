function breakoutGenerator(num, types, names, start, end, details) {
  const possibleLocations = locArr.filter((el) => {
    return el.type === "Breakout";
  });
  const events = [];
  for (let i = 0; i < num; i++) {
    const event = new Event(
      types[i],
      `${names[i]} #${breakout++} `,
      start,
      end,
      true,
      possibleLocations[i],
      details
    );

    events.push(event);
  }

  return events;
}
