function breakoutGenerator(num, start, end, details) {
  const possibleLocations = locArr.filter((el) => {
    return el.type === "Breakout";
  });
  const events = [];
  for (let i = 0; i < num; i++) {
    const event = new Event(
      1,
      `Breakout ${breakout++}`,
      start,
      end,
      true,
      possibleLocations[i],
      details
    );

    events.push(event);
  }

  return events;
}
